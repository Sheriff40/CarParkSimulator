package ssh.vs.entity;

public class Vechile {

	private int totalEntered;
	private int totalParking;
	private int totalExit;
	
	
	public int getTotalEntered() {
		return totalEntered;
	}
	public void setTotalEntered(int totalEntered) {
		this.totalEntered = totalEntered;
	}
	public int getTotalParking() {
		return totalParking;
	}
	public void setTotalParking(int totalParking) {
		this.totalParking = totalParking;
	}
	public int getTotalExit() {
		return totalExit;
	}
	public void setTotalExit(int totalExit) {
		this.totalExit = totalExit;
	}
	
	
}
